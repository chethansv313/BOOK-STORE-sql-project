-------CREATE TABLE BOOKS
CREATE TABLE BOOKS(
    BOOK_ID INT PRIMARY KEY,
    TITLE VARCHAR(200),
    AUTHOR VARCHAR(100),
    GENRE VARCHAR(50),
    PUBLISHED_YEAR INT,
    PRICE NUMERIC(10,2),
    STOCK INT
);


--CREATE TABLE CUSTOMER
CREATE TABLE CUSTOMERS(
CUSTOMER_ID INT PRIMARY KEY,
NAME VARCHAR(100),
EMAIL VARCHAR(100),
PHONE VARCHAR(20),
CITY VARCHAR(100),
COUNTRY VARCHAR(100)
);

----CREATE TABLE ORDERS
CREATE TABLE ORDERS(
    ORDER_ID INT PRIMARY KEY,
    CUSTOMER_ID INT,
    BOOK_ID INT,
    ORDER_DATE DATE,
    QUANTITY INT,
    TOTAL_AMOUNT NUMERIC(10,2),
	FOREIGN KEY (CUSTOMER_ID)
    REFERENCES CUSTOMERS(CUSTOMER_ID),
    FOREIGN KEY (BOOK_ID)
    REFERENCES BOOKS(BOOK_ID)
);


