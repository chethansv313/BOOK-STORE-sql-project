-----ADVANCED QUERIES

----TOTAL NUMBER OF BOOKS SOLD FOR EACH GENRE
SELECT GENRE,SUM(O.QUANTITY) AS TOTAL_SOLD
FROM ORDERS O
INNER JOIN 
BOOKS B
ON O.BOOK_ID=B.BOOK_ID
GROUP BY GENRE;

----AVERAGE PRICE OF BOOKS IN FANTASY GENRE
SELECT AVG(PRICE) AS AVERAGEPRICE
FROM BOOKS
WHERE GENRE='Fantasy';

----LIST OF CUSTOMER WHO HAVE PLACED ATLEAST 2 ORDERS
SELECT O.CUSTOMER_ID,C.NAME,COUNT(O.ORDER_ID)
FROM ORDERS O
INNER JOIN 
CUSTOMERS C
ON O.CUSTOMER_ID=C.CUSTOMER_ID
GROUP BY O.CUSTOMER_ID,C.NAME
HAVING COUNT(ORDER_ID)>=2;

------MOST FREQUENTLY ORDERED BOOK
SELECT B.TITLE,O.BOOK_ID,COUNT(O.ORDER_ID) AS ORDERED
FROM ORDERS O
INNER JOIN 
BOOKS B
ON O.BOOK_ID=B.BOOK_ID
GROUP BY O.BOOK_ID,B.TITLE
ORDER BY COUNT(O.ORDER_ID) DESC
limit 1;

----TOP 3 MOST EXPENSIVE BOOK FOR FANTASY GENRE
SELECT * 
FROM BOOKS
WHERE GENRE='Fantasy'
ORDER BY PRICE DESC
LIMIT 3;

----TOTAL QUANTITY SOLD BY EACH AUTHOR

SELECT B.AUTHOR,SUM(QUANTITY) AS TOTAL_QUANTITY
FROM ORDERS O
INNER JOIN
BOOKS B
ON O.BOOK_ID=B.BOOK_ID
GROUP BY B.AUTHOR;

----LIST THE CITY CUSTOMERS SPENT MORE THAN $30
SELECT  DISTINCT C.CITY,SUM(O.TOTAL_AMOUNT)
FROM ORDERS O
JOIN 
CUSTOMERS C
ON C.CUSTOMER_ID=O.CUSTOMER_ID
GROUP BY C.CITY
HAVING SUM(O.TOTAL_AMOUNT)>30; 

-----FIND THE CUSTOMER WHO SPENT MOST ON ORDERS
SELECT C.NAME,SUM(O.TOTAL_AMOUNT) AS SPENT
FROM ORDERS O
JOIN
CUSTOMERS C
ON C.CUSTOMER_ID=O.CUSTOMER_ID
GROUP BY C.NAME

ORDER BY SUM(O.TOTAL_AMOUNT) DESC
LIMIT 1;

---STOCK REMAINING AFTER FULFILLING ALL ORDER

SELECT B.TITLE,B.STOCK,COALESCE(SUM(O.QUANTITY),0) AS BOOKS_ORDERED_QUANTITY,B.STOCK-COALESCE(SUM(O.QUANTITY),0) AS QUANTITYREMAINING
FROM BOOKS B
LEFT JOIN
ORDERS O
ON O.BOOK_ID=B.BOOK_ID
GROUP BY B.BOOK_ID
ORDER BY B.BOOK_ID ASC;





